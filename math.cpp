// Ques- WAP to print square root, round number and log

#include <iostream>
#include <cmath>
using namespace std;
int main()
{
    cout << "Square root is: " << sqrt(90) << endl;
    cout << "Round number is: " << round(3.6) << endl;
    cout << "Log value is: " << log(20) << endl;
    return 0;
}
